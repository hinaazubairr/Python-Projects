# Program# 2: Write a program to print your favorite color 5 times in a row:
# One way to write this is to create 5 print() statements as below:
print ('1. blue')
print ('2. blue')
print ('3. blue')
print ('4. blue')
print ('5. blue')


# But this is very tiring way to write this print() statements again and again. 
# So, there is a better way to do this


# A VARIABLE: is a name that represents a value, such as number or piece of text (called a string).
# SYNTAX OF VARIABLE:      variable = value 
color = 'blue'
print(color)
# Now, if we want to write this color 5 times then:
print(color*5)
